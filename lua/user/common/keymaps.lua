Клавиатурные сокращения, которые работают в обоих режимах:
- VSCode/Windsurf (через расширение vscode-neovim)
- Standalone Neovim

ВАЖНО: Сюда входят ТОЛЬКО базовые горячие клавиши, которые работают
в обоих редакторах (простые vim команды без плагинов и API).

Специфичные для редакторов:
- Nvim-specific: lua/user/nvim/keymaps.lua
- Windsurf-specific: lua/user/windsurf/keymaps.lua

Стиль: AstroNvim
=====================================================================
--]]

local map = vim.keymap.set

-- =====================================================================
-- БАЗОВАЯ НАВИГАЦИЯ (работает везде, AstroNvim style)
-- =====================================================================

-- Перемещение по строкам с учетом переноса (AstroNvim style)
-- j/k перемещаются по визуальным строкам, а не логическим
map({ "n", "x" }, "j", "v:count == 0 ? 'gj' : 'j'", { expr = true, silent = true, desc = "Курсор вниз" })
map({ "n", "x" }, "k", "v:count == 0 ? 'gk' : 'k'", { expr = true, silent = true, desc = "Курсор вверх" })

-- Центрирование при поиске (AstroNvim style)
map("n", "n", "nzzzv", { desc = "Следующее совпадение и центр" })
map("n", "N", "Nzzzv", { desc = "Предыдущее совпадение и центр" })

-- =====================================================================
-- СТАНДАРТНЫЕ ОПЕРАЦИИ (работают везде)
-- =====================================================================

-- Сохранение файла (AstroNvim style)
map("n", "<C-s>", "<cmd>w<CR>", { desc = "Сохранить файл" })
