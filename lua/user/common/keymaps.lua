--[[
=====================================================================
                    ОБЩИЕ СОЧЕТАНИЯ КЛАВИШ
=====================================================================
Клавиатурные сокращения, которые работают в обоих режимах:
- VSCode/Windsurf (через расширение vscode-neovim)
- Standalone Neovim

ВАЖНО: Сюда входят ТОЛЬКО базовые горячие клавиши, которые работают
в обоих редакторах (простые vim команды без плагинов и API).

Специфичные для редакторов:
- Nvim-specific: lua/user/nvim/keymaps.lua
- Windsurf-specific: lua/user/windsurf/keymaps.lua
=====================================================================
--]]

local keymap = vim.keymap.set

-- =====================================================================
-- БАЗОВАЯ НАВИГАЦИЯ (работает везде)
-- =====================================================================

-- Перемещение по строкам с учетом переноса
keymap({ "n", "x" }, "j", "v:count == 0 ? 'gj' : 'j'", { expr = true, silent = true, desc = "Курсор вниз" })
keymap({ "n", "x" }, "k", "v:count == 0 ? 'gk' : 'k'", { expr = true, silent = true, desc = "Курсор вверх" })

-- Центрирование при поиске
keymap("n", "n", "nzzzv", { desc = "Следующее совпадение и центр" })
keymap("n", "N", "Nzzzv", { desc = "Предыдущее совпадение и центр" })

-- =====================================================================
-- СТАНДАРТНЫЕ ОПЕРАЦИИ (работают везде)
-- =====================================================================

-- Сохранение файла
keymap("n", "<C-s>", "<cmd>w<CR>", { desc = "Сохранить" })

print("✓ Общие сочетания клавиш загружены (работают в nvim и Windsurf)")
