--[[
=====================================================================
                        VIM-REPEAT
=====================================================================
Плагин для расширенной поддержки повтора команд с помощью точки (.)

Проблема:
  Стандартная команда повтора (.) работает только с встроенными
  командами Vim. Команды плагинов не повторяются.
  
Решение:
  vim-repeat позволяет плагинам регистрировать свои команды
  для повтора с помощью точки.

Работает с:
  - nvim-surround (ys, ds, cs)
  - flash.nvim
  - и многими другими плагинами

GitHub: https://github.com/tpope/vim-repeat
=====================================================================
--]]

return {
  "tpope/vim-repeat",
  event = "VeryLazy",
}
