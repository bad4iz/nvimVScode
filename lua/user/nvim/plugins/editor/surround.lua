-- Плагин nvim-surround
-- Работа с окружающими символами: скобками, кавычками, тегами
-- Быстрое добавление, изменение и удаление окружающих символов
--
-- Основные операции:
--   ys{motion}{char} - Добавить окружение (например: ysiw" - окружить слово кавычками)
--   ds{char}         - Удалить окружение (например: ds" - удалить кавычки)
--   cs{old}{new}     - Изменить окружение (например: cs"' - заменить " на ')
--
-- Примеры:
--   ysiw)  - окружить слово скобками: word → (word)
--   dst    - удалить HTML тег: <div>text</div> → text
--   cs"'   - заменить кавычки: "text" → 'text'
return {
  {
    "kylechui/nvim-surround",
    event = "VeryLazy",
    config = function() require("nvim-surround").setup {} end,
  },
}
