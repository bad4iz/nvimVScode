-- Плагин vim-visual-multi
-- Множественные курсоры для редактирования нескольких мест одновременно
-- Аналог функции Multiple Cursors из VSCode
--
-- Основные горячие клавиши (по умолчанию):
--   <C-n>     - Выделить слово под курсором и добавить следующее вхождение
--   <C-Down>  - Добавить курсор ниже
--   <C-Up>    - Добавить курсор выше
--   n/N       - Перейти к следующему/предыдущему курсору
--   q         - Пропустить текущее и перейти к следующему
--   Q         - Удалить текущий курсор
--
-- Полезно для одновременного редактирования множества похожих мест в коде
